import java.io.*;
 
class swap {
    public static void main(String[] args)
    {
        int x;
        int y;
 
        // Code to swap 'x' and 'y'
        x = x * y; 
        y = x / y; 
        x = x / y; 
 
        System.out.println("After swaping:"
                           + " x = " + x + ", y = " + y);
    }
}
 
